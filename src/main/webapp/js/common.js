/*************************************
 * @함수명 : isNumber
 * @설명 : 인자가 숫자인지 판단
 * @return : 인자가 숫자이면 true / 아니면 false   
 *************************************/
function isNumber(input){
	var reg = /^[0-9]*$/;
	return reg.test(input);
}

